    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        HttpSession ses = request.getSession();
        if (ses.getAttribute("authen")!= null) {
            if((int)ses.getAttribute("authen")==-1) {
                response.sendRedirect("index.html");
            }else{
                request.getRequestDispatcher("Member01").forward(request, response);
            }
        }else {
            //response.sendRedirect("index.html");
            request.getRequestDispatcher("index.html").forward(request, response);
            
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        String name = request.getParameter("name");
        String pass = request.getParameter("pass");
        HttpSession ses = request.getSession();
        ses.setMaxInactiveInterval(20);
        if(name.equals("abc") && pass.equals("123")) {
            int roll = 0;
            ses.setAttribute("authen", roll);
            Cookie c = new Cookie("MyAuthen", roll+"");
            c.setMaxAge(40);
            response.addCookie(c);
            request.getRequestDispatcher("Member01").forward(request, response);
        } else {
            //response.sendRedirect("index.html");
            request.getRequestDispatcher("index.html").forward(request, response);
        }
        //request.getRequestDispatcher("Views/Login.jsp").forward(request, response);
    }

